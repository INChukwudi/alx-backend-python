#!/usr/bin/env python3

"""
File that holds the type-annotated add function
"""


def add(a: float, b: float) -> float:
    """
    type-annotated function add

    params:
    a (float) - number to be added
    b (float) - number to be added

    return:
    value (float) - sum of @a and @b
    """
    return a + b
